import mongoose from "mongoose";

const connectionMessageSchema = new mongoose.Schema(
  {
    requester: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    recipient: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    status: {
      type: String,
      enum: ["pending", "accepted", "rejected"],
      default: "pending",
    },
  },
  { timestamps: true }
);

const ConnectionMessage = mongoose.model(
  "ConnectionMessage",
  connectionMessageSchema
);

export default ConnectionMessage;
